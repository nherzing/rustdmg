pub const CLOCK_FREQ: u32 = 1024 * 1024;
pub const CLOCKS_PER_SCREEN_REFRESH: u32 = 17556;
pub const NS_PER_SCREEN_REFRESH: u32 = 1_000_000_000 / (CLOCK_FREQ / CLOCKS_PER_SCREEN_REFRESH);

pub const SERIAL_BIT_SHIFT_FREQ: u32 = 8192;
pub const CLOCKS_PER_SERIAL_BIT_SHIFT: u32 = CLOCK_FREQ / SERIAL_BIT_SHIFT_FREQ;
