pub const CLOCK_FREQ: u32 = 1024 * 1024;
pub const CLOCKS_PER_SCREEN_REFRESH: u32 = 17556;
pub const NS_PER_SCREEN_REFRESH: u64 = 1_000_000_000 / (CLOCK_FREQ / CLOCKS_PER_SCREEN_REFRESH) as u64;

pub const SERIAL_BIT_SHIFT_FREQ: u32 = 8192;
pub const CLOCKS_PER_SERIAL_BIT_SHIFT: u32 = CLOCK_FREQ / SERIAL_BIT_SHIFT_FREQ;

pub const AUDIO_SAMPLE_RATE: u32 = 48_000;
pub const SAMPLES_PER_FRAME: u32 = CLOCKS_PER_SCREEN_REFRESH * 2;
pub const NS_PER_SAMPLE: u64 = 1_000_000_000 / AUDIO_SAMPLE_RATE as u64;
